/// Agent loop creates a session/agent state that persists across calls.
#[tokio::test]
async fn test_agent_session_state_persists() {
    let provider = MockJsProvider::new(r#"return "ok";"#);
    let config = test_config();
    let sandbox = Sandbox::new_threaded(config.sandbox.clone());
    let store = Arc::new(Mutex::new(
        Store::open(":memory:").expect("in-memory store"),
    ));

    let session = kittypaw_cli::agent_loop::AgentSession {
        provider: &provider,
        fallback_provider: None,
        sandbox: &sandbox,
        store: store.clone(),
        config: &config,
        on_token: None,
        on_permission_request: None,
    };

    // First call creates state
    let r1 = session.run(desktop_event("first")).await;
    assert!(r1.is_ok());

    // Second call reuses state — agent should have 2 user turns + 2 assistant turns
    let r2 = session.run(desktop_event("second")).await;
    assert!(r2.is_ok());

    // Verify state was persisted
    let s = store.lock().await;
    let state = s
        .load_state("desktop-test")
        .expect("load_state")
        .expect("state should exist");
    // 2 user turns + 2 assistant turns = 4 total
    assert!(
        state.turns.len() >= 4,
        "expected >= 4 turns, got {}",
        state.turns.len()
    );
}

