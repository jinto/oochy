// ── Mock LLM Provider ──────────────────────────────────────────────────

/// A mock provider that returns pre-determined JS code as if generated by an LLM.
struct MockJsProvider {
    js_code: String,
}

impl MockJsProvider {
    fn new(js: &str) -> Self {
        Self {
            js_code: js.to_string(),
        }
    }
}

#[async_trait]
impl LlmProvider for MockJsProvider {
    async fn generate(&self, _messages: &[LlmMessage]) -> Result<LlmResponse> {
        Ok(LlmResponse::text_only(self.js_code.clone()))
    }
}

