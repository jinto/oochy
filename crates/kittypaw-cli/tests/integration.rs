/// Agent loop processes a simple return statement and produces output.
#[tokio::test]
async fn test_agent_loop_simple_return() {
    let provider = MockJsProvider::new(r#"return "hello from agent";"#);
    let config = test_config();
    let sandbox = Sandbox::new_threaded(config.sandbox.clone());
    let store = Arc::new(Mutex::new(
        Store::open(":memory:").expect("in-memory store"),
    ));

    let session = kittypaw_cli::agent_loop::AgentSession {
        provider: &provider,
        fallback_provider: None,
        sandbox: &sandbox,
        store: store.clone(),
        config: &config,
        on_token: None,
        on_permission_request: None,
    };

    let event = desktop_event("say hello");
    let result = session.run(event).await;
    assert!(result.is_ok(), "agent_loop should succeed: {:?}", result);
    assert_eq!(result.unwrap(), "hello from agent");
}

