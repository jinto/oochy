/// Scheduled skill WITHOUT try/catch that calls Http.get — bare execute
/// (without resolver) crashes with "Exception generated by QuickJS".
/// With resolver, it should succeed.
#[tokio::test]
async fn test_scheduled_skill_bare_http_no_trycatch() {
    let config = test_config();
    let sandbox = Sandbox::new_threaded(config.sandbox.clone());
    let store = Arc::new(Mutex::new(
        Store::open(":memory:").expect("in-memory store"),
    ));

    // No try/catch — this is what causes "Exception generated by QuickJS"
    let js_code = r#"
        const resp = await Http.get("https://httpbin.org/get");
        return "status:" + resp.status;
    "#;
    let context = serde_json::json!({
        "event_type": "schedule",
        "event_text": "",
        "chat_id": "",
    });
    let wrapped = format!("const ctx = JSON.parse(__context__);\n{js_code}");

    // Step 1: bare execute WITHOUT resolver — must fail (this was the bug)
    let bare_result = sandbox.execute(&wrapped, context.clone()).await;
    assert!(bare_result.is_ok());
    let bare_exec = bare_result.unwrap();
    assert!(
        !bare_exec.success,
        "bare execute without resolver SHOULD fail (null.status)"
    );

    // Step 2: execute WITH resolver — must succeed (the fix)
    let config_clone = config.clone();
    let store_clone = Arc::clone(&store);
    let resolver: kittypaw_sandbox::SkillResolver =
        std::sync::Arc::new(move |call: kittypaw_core::types::SkillCall| {
            let config = config_clone.clone();
            let store = Arc::clone(&store_clone);
            Box::pin(async move {
                kittypaw_engine::skill_executor::resolve_skill_call(
                    &call, &config, &store, None, None,
                )
                .await
            })
        });

    let resolved_result = sandbox
        .execute_with_resolver(&wrapped, context, Some(resolver))
        .await;
    assert!(
        resolved_result.is_ok(),
        "sandbox with resolver should not error: {:?}",
        resolved_result
    );
    let resolved_exec = resolved_result.unwrap();
    assert!(
        resolved_exec.success,
        "with resolver, Http.get should work. error={:?}, output={}",
        resolved_exec.error, resolved_exec.output
    );
}

